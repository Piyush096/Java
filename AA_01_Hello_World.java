class AA_01_Hello_World
{
    public static void main(String args[])
    {
        System.out.print("hello\t");
        System.out.println("hello");
        System.out.println("hello\n");
    }
}
/*
*public static void main(String args[]):- this is the line at which the program will begin executing,
as a general rule , a java programs begins execution by calling main().

*
*using println java will put a new line at the end . but in case of print java will not put new line.

*/
